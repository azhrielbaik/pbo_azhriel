<?php
// Contoh 1: Modulus
$x = 10;
$y = 6;
echo "Hasil $x % $y = " . ($x % $y) . "<br>";

// Contoh 2: Modulus lagi (sama dengan contoh 1)
$x = 10;
$y = 6;
echo "Hasil $x % $y = " . ($x % $y) . "<br>";

// Contoh 3: Pangkat
$x = 10;
$y = 3;
echo "Hasil $x ** $y = " . ($x ** $y) . "<br>";

// Contoh 4: Perkalian dengan assignment (*=)
$x = 5;
$x *= 6;   // sama dengan $x = $x * 6
echo "Hasil 5 *= 6 = " . $x . "<br>";

// Contoh 5: Modulus dengan assignment (%=)
$x = 15;
$x %= 4;   // sama dengan $x = $x % 4
echo "Hasil 15 %= 4 = " . $x . "<br>";
?>





